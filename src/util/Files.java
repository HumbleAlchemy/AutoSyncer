package util;

public class Files {
	private String file;
	private String parent;
	private String name;
	private String user_cloud_name;
	private String user_cloudID;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getUser_cloud_name() {
		return user_cloud_name;
	}
	public void setUser_cloud_name(String user_cloud_name) {
		this.user_cloud_name = user_cloud_name;
	}
	public String getUser_cloudID() {
		return user_cloudID;
	}
	public void setUser_cloudID(String user_cloudID) {
		this.user_cloudID = user_cloudID;
	}
	public String getFile() {
		return file;
	}
	public void setFile(String file) {
		this.file = file;
	}
	public String getParent() {
		return parent;
	}
	public void setParent(String parent) {
		this.parent = parent;
	}
	
	
}
